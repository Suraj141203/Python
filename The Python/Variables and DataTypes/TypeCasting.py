# Type Casting

# Integer to Float
a = 10
b = float(a)
print("Integer to Float:", b)

# Float to Integer
c = 5.7
d = int(c)
print("Float to Integer:", d)

# String to Integer
e = "25"
f = int(e)
print("String to Integer:", f)

# Integer to String
g = 100
h = str(g)
print("Integer to String:", h)

# Check Types
print("Type of b:", type(b))
print("Type of d:", type(d))
print("Type of f:", type(f))
print("Type of h:", type(h))
