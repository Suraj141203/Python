# Escape Characters Example

# New Line and Tab
a = "Suraj is a Good Boy\nBut Not a\tBad Boy"

# Double Quote inside string
b = "I Am A \"Python\" Learner"

# Single Quote inside string
c = 'It\'s my Python journey'

# Backslash
d = "This is a backslash: \\"

# Backspace (removes one character before it)
e = "Hello\bWorld"

# Carriage Return (overwrites from beginning)
f = "Hello\rHi"

# Form Feed
g = "Hello\fWorld"

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
